
public class TableOfSquares {


	public static void main(String[] args) 
	{
		int number = 0;
		
		System.out.printf("%s %s %s\n", "number", "square", "cube");
		System.out.printf("%d       %d       %d\n", number, number*number, number*number*number);
		System.out.printf("%d       %d       %d\n", (number+1), (number+1)*(number+1), (number+1)*(number+1)*(number+1));
		System.out.printf("%d       %d       %d\n", (number+2), (number+2)*(number+2), (number+2)*(number+2)*(number+2));
		System.out.printf("%d       %d       %d\n", (number+3), (number+3)*(number+3), (number+3)*(number+3)*(number+3));

	}//main

}//TableOfSquares Class
